const pill = document.getElementById("scroll-pill");
const track = document.getElementById("scroll-track");

let fadeTimeout; // This holds our timer

const updatePill = () => {
  // 1. Show the track immediately when scrolling starts
  track.classList.add("visible");

  // 2. Clear the previous timer (so it doesn't fade while we are still moving)
  clearTimeout(fadeTimeout);

  // 3. Do the math we did before
  const scrolled = window.scrollY;
  const maxScroll = document.documentElement.scrollHeight - window.innerHeight;

  if (maxScroll > 0) {
    const scrollPercent = scrolled / maxScroll;
    const travelDistance = track.offsetHeight - pill.offsetHeight;
    const finalPos = scrollPercent * travelDistance;
    pill.style.transform = `translateY(${finalPos}px)`;
  }

  // 4. Set a new timer to hide the track after 1.5 seconds of no scrolling
  fadeTimeout = setTimeout(() => {
    track.classList.remove("visible");
  }, 1500); // 1500ms = 1.5 seconds
};

window.addEventListener("scroll", updatePill, { passive: true });

let currentlang = "en";

function setLanguage(lang) {
  currentlang = lang;
  /*header*/
  document.getElementById("header-label").innerText =
    language[lang].header.label;

  /*hero*/
  document.getElementById("hero-anchor-phrase").innerText =
    language[lang].hero.anchor;
  document.getElementById("hero-title").innerText = language[lang].hero.title;

  /*About-Product*/
  document.getElementById("about-Product-title").innerText =
    language[lang].aboutProduct.title;
  Object.keys(language[lang].aboutProduct.weeks).forEach((key, i) => {
    document.querySelectorAll("#about-Product p")[i].innerText =
      language[lang].aboutProduct.weeks[key].label;

    document
      .querySelectorAll("#about-Product ul")
      [i].querySelectorAll("li")
      .forEach((el, i) => {
        el.innerText = language[lang].aboutProduct.weeks[key].topics[i];
      });
  });

  /*about-owner*/
  document.getElementById("about-owner-title").innerText =
    language[lang].aboutOwner.title;
  document.querySelectorAll(".about-owner-paragraph").forEach((el, i) => {
    el.innerText = language[lang].aboutOwner.paragraphs[i];
  });

  /*pricing*/
  document.getElementById("pricing-title").innerText =
    language[lang].pricing.title;
  Object.keys(language[lang].pricing.packages).forEach((key, i) => {
    document.querySelectorAll(".pricing-name")[i].innerText =
      language[lang].pricing.packages[key].name;

    document.querySelectorAll(".pricing-sessions")[i].innerText =
      language[lang].pricing.packages[key].sessions;

    document.querySelectorAll(".pricing-price")[i].innerText =
      language[lang].pricing.packages[key].price;
  });
  document.querySelector(".pricing-duration").innerText =
    language[lang].pricing.packages.main.duration;
  document.getElementById("pricing-include").innerText =
    language[lang].pricing.packages.main.include.label;
  document.getElementById("pricing-include-item").innerText =
    language[lang].pricing.packages.main.include.topic;

  document.querySelectorAll(".payment-info").forEach((el, i) => {
    el.innerHTML = language[lang].pricing.payment[i];
  });

  /*footer*/
  document.querySelectorAll(".footer-title").forEach((el, i) => {
    el.innerText = language[lang].footer.titles[i];
  });
}

document
  .getElementById("lang-en")
  .addEventListener("click", () => setLanguage("en"));
document
  .getElementById("lang-tr")
  .addEventListener("click", () => setLanguage("tr"));

